package net.ccmob.apps.jpushy.utils;

public class Coord2D {
	/**
	 * 
	 * @author Marcel Benning
	 * 
	 */
	private int x = 0, y = 0;
	
	public Coord2D(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
